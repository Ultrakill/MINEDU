    private void tblRolesMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRolesMouseReleased
        // TODO add your handling code here:
        int fila = tblRoles.getSelectedRow();
        if (fila != -1) {
            Rol seleccionada = rolList.get(fila);
            if (evt.getClickCount() == 1) {
                mostrar(seleccionada);
            } else if (evt.getClickCount() == 2) {
                rolSeleccionado = seleccionada;
                this.dispose();
            }
        }


    }//GEN-LAST:event_tblRolesMouseReleased

    public Rol getRolSeleccionado() {
        this.setVisible(true);
        return rolSeleccionado;
    }

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (FormularioUtil.dialogoConfirmar(this, accion)) {
            Rol seleccionada = rolControlador.getSeleccionado();

            if (accion == Controlador.NUEVO) {
                seleccionada.setCodigo(txtCodigo.getText());
            }
            seleccionada.setNombre(txtNombre.getText());
            

            if (rolControlador.accion(accion)) {
                FormularioUtil.mensajeExito(this, accion);
                this.accion = 0;
                this.controles(accion);
                this.actualizarTabla();
            } else {
                FormularioUtil.mensajeError(this, accion);
            }

        } else {
            return;
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        int fila = tblRoles.getSelectedRow();
        if(fila != -1){
            this.accion = Controlador.MODIFICAR;
            this.rolControlador.setSeleccionado(this.rolList.get(fila));
            mostrar(this.rolControlador.getSeleccionado());
            controles(accion);
            this.txtCodigo.setEditable(false);
            mtAcceso.setRolSeleccionado(rolControlador.getSeleccionado());
            mtAcceso.setDatosExistentes(rolControlador.getSeleccionado().getRolAccesoList());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

