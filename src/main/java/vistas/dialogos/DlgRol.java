    private void modeloHoraSpinner(JSpinner spinner) {
        JSpinner.DateEditor editor = new JSpinner.DateEditor(spinner, "HH:mm");
        DateFormatter formatter = (DateFormatter) editor.getTextField().getFormatter();
        formatter.setAllowsInvalid(false); // this makes what you want
        formatter.setOverwriteMode(true);
        spinner.setEditor(editor);
        Calendar cal = Calendar.getInstance();
        cal.set(Calendar.HOUR, 7);
        cal.set(Calendar.MINUTE, 30);
        cal.set(Calendar.SECOND, 0);
        cal.set(Calendar.AM_PM, Calendar.AM);
        spinner.setValue(cal.getTime());
    }

    private void modeloFechaSpinner(JSpinner spinner) {
        JSpinner.DateEditor editor = new JSpinner.DateEditor(spinner, "dd/MM/yyyy");
        DateFormatter formatter = (DateFormatter) editor.getTextField().getFormatter();
        formatter.setAllowsInvalid(false); // this makes what you want
        formatter.setOverwriteMode(true);
        spinner.setEditor(editor);
    }

    private void controles(int accion) {
        boolean bandera = accion == Controlador.NUEVO || accion == Controlador.MODIFICAR;

        FormularioUtil.activarComponente(this.pnlListado, !bandera);
        FormularioUtil.activarComponente(this.pnlDatosJornada, bandera);

        if (accion != Controlador.MODIFICAR) {
            FormularioUtil.limpiarComponente(this.pnlDatosJornada);
        }

    }
    
    private MTAcceso mtAcceso;
    private AccesoControlador ac = new AccesoControlador();
    private void bindeoSalvaje() {
        rolList = rolControlador.buscarTodos();  
        rolList = ObservableCollections.observableList(rolList);
        List<Acceso> accesos = ac.buscarTodos();
        mtAcceso = new MTAcceso(accesos, new ArrayList<RolAcceso>(), rolSeleccionado);
        MTRol mtRol = new MTRol(rolList);
        this.tblRoles.setModel(mtRol);
        tblAccesos.setModel(mtAcceso);
    }

    private void mostrar(Rol seleccionado) {
        txtCodigo.setText(seleccionado.getCodigo());
        txtNombre.setText(seleccionado.getNombre());
        mtAcceso.setDatosExistentes(seleccionado.getRolAccesoList());
    }

    private void actualizarTabla() {
        rolList.clear();
        rolList.addAll(rolControlador.buscarTodos());
    }
